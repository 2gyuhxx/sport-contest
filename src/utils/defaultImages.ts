// 이미지가 없을 때 적절한 기본 이미지를 반환하는 함수
export function getDefaultImage(
  subSport: string | null | undefined,
  sport: string | null | undefined,
  category: string
): string {
  // 1. 소분류 종목 이미지 우선
  if (subSport && sportDefaultImages[subSport]) {
    return sportDefaultImages[subSport]
  }
  
  // 2. 대분류 종목 이미지
  if (sport && sportDefaultImages[sport]) {
    return sportDefaultImages[sport]
  }
  
  // 3. 카테고리 기본 이미지
  if (categoryDefaultImages[category]) {
    return categoryDefaultImages[category]
  }
  
  // 4. 최종 기본 이미지
  return '/images/main_logo.png'
}

